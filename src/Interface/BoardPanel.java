    //------ Creation des cellules -----------
    public void creation() {
        
        //------ L'instanciation 
        board = new Cellule[64];     
        
        int count = 0;
        int k = 0;
        
        /***********************************************************************
         * Nous avons cre√© une tables des celllules, chaque ligne de "board" va
         * contenir 8 cellules et de meme, chaque colonne va contenir 8 cases,
         * pour ceci, on a besoin de deux boucles pour dessiner 8 cellules sur 
         * la meme ligne, puis revenir a la ligne et dessines 8 cellules aussi, 
         * et ainsi de suite
         **********************************************************************/
        for(int i = 0; i < 8; i++)
        {
            for(int j = 0; j < 8; j++)
            {
                count++;
                if(count % 9 == 0)
                    break;
                board[k] = new Cellule(k);
                board[k].setBounds(j * Cellule.TAILLE_CELL, i * Cellule.TAILLE_CELL, Cellule.TAILLE_CELL, Cellule.TAILLE_CELL);
                board[k].addMouseListener(board[k]);
                this.add(board[k]);
                k++;
            }
            count = 0;
        }
        
        //------- dessiner les 4 pions de debut
        board[27].drawPion(Color.white);
        board[28].drawPion(Color.black);
        board[35].drawPion(Color.black);
        board[36].drawPion(Color.white);
        
        this.repaint();
    }
